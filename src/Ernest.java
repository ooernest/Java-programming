public class Ernest {
    private String name;
    private int age;
    private String skinColour;
    private boolean isOld;
    private float accountBalance;

    public Ernest (){
        // Empty constructor
    }

    public Ernest (String name, int age, String skinColour, boolean isOld, float accountBalance){
        this.accountBalance =accountBalance;
        this.name = name;
        this.age =age;
        this.skinColour =skinColour;
        this.isOld =isOld;
    }
}
